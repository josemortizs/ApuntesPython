# Introducció a las cadenas de caracteres

# Concatenación:
cadena1 = "Hola, "
cadena2 = "¿Cómo estás?"
cadena3 = cadena1 + cadena2
print(cadena3) # salida: Hola, ¿Cómo estás?

# Multiplicación: 
cadena4 = cadena1 * 4
print(cadena4) # salida: Hola, Hola, Hola, Hola, 

# Indexación:
print(cadena1[0]) # salida: H

# Longitud:
print(len(cadena1)) # salida: 6


